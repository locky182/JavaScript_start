//Операторы присваивания
let age = 18 + 2;
age++;// можно age += 2 это равно как age = age + 2
age += 4;// age = age + 4
age *= 6;// age = age * 6
age /= 2;// age = age / 2
age++;
console.log(age);

//Операторы срвнения
const vasya = 20;
console.log(age > vasya);
console.log(age >= vasya);
console.log(age < vasya);
console.log(age <= vasya);
console.log(age == vasya);
